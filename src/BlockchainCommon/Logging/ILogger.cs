{

public enum Level
{
  FATAL = 0,
  ERROR = 1,
  WARNING = 2,
  INFO = 3,
  DEBUGGING = 4,
  TRACE = 5
}

public interface ILogger
{
  public readonly char COLOR_DELIMETER;

  public readonly List<string> LEVEL_NAMES = new List<string>();

  void operator ()(string category, Level level, boost::posix_time.ptime time, string body);
}

